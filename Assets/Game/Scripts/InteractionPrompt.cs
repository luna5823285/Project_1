// 상호작용 가능한 오브젝트 위에 'E' 표시를 관리하는 스크립트
// PlayerInteraction에서 플레이어가 근처에 있을 때 표시
// ============================================================

using UnityEngine;
using TMPro;

public class InteractionPrompt : MonoBehaviour
{
    // ============================================================
    // Inspector 설정 변수
    // ============================================================
    
    // 표시할 TextMeshProUGUI (Canvas에 있어야 함)
    public TextMeshProUGUI promptText;

    // ============================================================
    // Unity 생명주기 - Start
    // ============================================================
    private void Start()
    {
        // 초기에는 숨김
        Hide();
    }

    // ============================================================
    // 표시 메서드
    // ============================================================
    public void Show()
    {
        if (promptText != null)
        {
            promptText.enabled = true;
        }
    }

    // ============================================================
    // 숨김 메서드
    // ============================================================
    public void Hide()
    {
        if (promptText != null)
        {
            promptText.enabled = false;
        }
    }
}
