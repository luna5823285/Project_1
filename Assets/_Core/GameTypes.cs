// 열쇠 타입: 플레이어가 획득할 수 있는 열쇠의 종류
public enum KeyType
{
    A,  // 열쇠 A (서랍에서 획득, door_2 → Ending_A)
    B,  // 열쇠 B (서랍에서 획득, door_2 → Ending_A)
    C   // 열쇠 C (액자 퍼즐에서 획득, door_2 → Ending_B)
}

// 문 타입: 씬에 배치되는 문의 종류
public enum DoorType
{
    Move,    // 이동용 문: 다음 씬으로 이동 (door_1: Game_Map_1 → Game_Map_2)
    Ending   // 엔딩용 문: 열쇠 선택 → 엔딩 씬으로 이동 (door_2)
}
