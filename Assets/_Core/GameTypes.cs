// 열쇠 타입: 플레이어가 획득할 수 있는 열쇠의 종류
public enum KeyType
{
    A,  // 열쇠 A (잘못된 열쇠)
    B   // 열쇠 B (올바른 열쇠)
}

// 문 타입: 씬에 배치되는 문의 종류
public enum DoorType
{
    Move,    // 이동용 문: 다음 씬으로 이동 (0.0.1에서는 미사용)
    Ending   // 엔딩용 문: 올바른 열쇠로 열면 엔딩 씬으로 이동
}
